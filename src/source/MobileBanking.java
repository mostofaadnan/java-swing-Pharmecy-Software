 private void table_update() throws SQLException {
        DefaultTableModel model2 = (DefaultTableModel) datatbl.getModel();
        model2.setRowCount(0);
        int tree = 0;
        try {
             String sql = "Select inputdate,Type,mobileNo,remark,cashIn,cashOut from mobilebankbalance";

            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();

            while (rs.next()) {
                String inputdate = rs.getString("inputdate");
                String type=rs.getString("Type");
                String mobileno=rs.getString("mobileNo");
                String remark=rs.getString("remark");
                float cashin=rs.getFloat("cashIn");
                float cashout=rs.getFloat("cashOut");
                float balance=cashin-cashout;
                tree++;
                model2.addRow(new Object[]{tree, inputdate, mobileno, type, remark, cashin, cashout, balance});
                

            }

            
            
            } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);

        }
        
        totalAction();
        }
 
 
 
