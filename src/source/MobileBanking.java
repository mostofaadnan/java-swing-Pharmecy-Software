  private void TotalBalance() throws SQLException {
        
        try {
             String sql = "Select sum(cashIn) as 'cashIn' ,sum(cashOut) as 'cashOut' from mobilebankbalance";

            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();

            while (rs.next()) {
                
                float cashin=rs.getFloat("cashIn");
                float cashout=rs.getFloat("cashOut");
                float balance=cashin-cashout;
                totalbalancetext.setText(df2.format(balance));
              
                

            }

            
            
            } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);

        }
        }
  
